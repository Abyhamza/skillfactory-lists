#Используя вложенные циклы for, напишите программу, которая выводит все возможные пары сумма которых равна заданному числу n.
# Значение n должно быть предварительно задано.
n = 5
for i in range(1, n):
    for j in range(1, n)[::-1]:
        if i + j == n:
            print(f"{i}, {j}")